package com.javajo.project.dto;

public class FacDTO {
	private String hfac;
	private String hfac_des;
	private String hfac_image;
	
	public String getHfac() {
		return hfac;
	}
	public void setHfac(String hfac) {
		this.hfac = hfac;
	}
	public String getHfac_des() {
		return hfac_des;
	}
	public void setHfac_des(String hfac_des) {
		this.hfac_des = hfac_des;
	}
	public String getHfac_image() {
		return hfac_image;
	}
	public void setHfac_image(String hfac_image) {
		this.hfac_image = hfac_image;
	}
}
